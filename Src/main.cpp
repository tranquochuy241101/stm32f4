void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == htim5.Instance)
	{
			cnt ++;
			Encoder_cnt = __HAL_TIM_GET_COUNTER(&htim3);
			v = (Encoder_cnt*3000/960);
			e = Setpoint_value - v;									// e(t)=r(t) - v(t)
			P_control = Kp*(e - e_prev);							// Kp*(e(k) - e(k-1)
			I_control = Ki*0.01*(e + e_prev);						// Ki*T/2*(e(k) + e(k-1))
			if(cnt==1)
				u= Setpoint_value;
			else
				u = u_prev + P_control + I_control;						// next signal to motor
			e_prev = e;
			u_prev = u;
			// Send and recieve data from ROS
			loop();

			//Convert signal in RPM to duty cycle
			duty = (int)(u*600/208);				// u/176 = x/600 => x = u*600/176

			// New PWM width for motor
			htim2.Instance->CCR1 = duty;
			__HAL_TIM_SET_COUNTER(&htim3,0);						// Reset value of encoder
	}
}

